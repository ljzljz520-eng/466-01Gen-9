/**
 * This is a API server
 */

import express, {
  type Request,
  type Response,
  type NextFunction,
} from 'express'
import cors from 'cors'
import path from 'path'
import dotenv from 'dotenv'
import { fileURLToPath } from 'url'
import companiesRoutes from './routes/companies.js'
import elevatorsRoutes from './routes/elevators.js'
import faultsRoutes from './routes/faults.js'
import reportsRoutes from './routes/reports.js'
import rectificationsRoutes from './routes/rectifications.js'
import remindersRoutes from './routes/reminders.js'

// for esm mode
const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

// load env
dotenv.config()

const app: express.Application = express()

app.use(cors())
app.use(express.json({ limit: '10mb' }))
app.use(express.urlencoded({ extended: true, limit: '10mb' }))

/**
 * API Routes
 */
app.use('/api/companies', companiesRoutes)
app.use('/api/elevators', elevatorsRoutes)
app.use('/api/faults', faultsRoutes)
app.use('/api/reports', reportsRoutes)
app.use('/api/rectifications', rectificationsRoutes)
app.use('/api/reminders', remindersRoutes)

/**
 * health
 */
app.use(
  '/api/health',
  (req: Request, res: Response, next: NextFunction): void => {
    res.status(200).json({
      success: true,
      message: 'ok',
    })
  },
)

/**
 * error handler middleware
 */
app.use((error: Error, req: Request, res: Response, next: NextFunction) => {
  res.status(500).json({
    success: false,
    error: 'Server internal error',
  })
})

/**
 * 404 handler
 */
app.use((req: Request, res: Response) => {
  res.status(404).json({
    success: false,
    error: 'API not found',
  })
})

export default app
